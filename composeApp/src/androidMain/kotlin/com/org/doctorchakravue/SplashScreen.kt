// Path command parser
sealed class PathCommand {
    data class MoveTo(val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    data class LineTo(val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    data class HorizontalLineTo(val x: Float, val relative: Boolean) : PathCommand()
    data class VerticalLineTo(val y: Float, val relative: Boolean) : PathCommand()
    data class CurveTo(val x1: Float, val y1: Float, val x2: Float, val y2: Float, val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    data class SmoothCurveTo(val x2: Float, val y2: Float, val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    data class QuadTo(val x1: Float, val y1: Float, val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    data class SmoothQuadTo(val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    data class ArcTo(val rx: Float, val ry: Float, val xAxisRotation: Float, val largeArcFlag: Boolean, val sweepFlag: Boolean, val x: Float, val y: Float, val relative: Boolean) : PathCommand()
    object ClosePath : PathCommand()
}

fun parsePathData(pathData: String): List<PathCommand> {
    val commands = mutableListOf<PathCommand>()
    val regex = Regex("([MmLlHhVvCcSsQqTtAaZz])([^MmLlHhVvCcSsQqTtAaZz]*)")

    for (match in regex.findAll(pathData)) {
        val command = match.groupValues[1]
        val params = match.groupValues[2].trim()
        val numbers = parseNumbers(params)

        when (command) {
            "M" -> {
                var i = 0
                while (i < numbers.size - 1) {
                    commands.add(PathCommand.MoveTo(numbers[i], numbers[i + 1], false))
                    i += 2
                }
            }
            "m" -> {
                var i = 0
                while (i < numbers.size - 1) {
                    commands.add(PathCommand.MoveTo(numbers[i], numbers[i + 1], true))
                    i += 2
                }
            }
            "L" -> {
                var i = 0
                while (i < numbers.size - 1) {
                    commands.add(PathCommand.LineTo(numbers[i], numbers[i + 1], false))
                    i += 2
                }
            }
            "l" -> {
                var i = 0
                while (i < numbers.size - 1) {
                    commands.add(PathCommand.LineTo(numbers[i], numbers[i + 1], true))
                    i += 2
                }
            }
            "H" -> numbers.forEach { commands.add(PathCommand.HorizontalLineTo(it, false)) }
            "h" -> numbers.forEach { commands.add(PathCommand.HorizontalLineTo(it, true)) }
            "V" -> numbers.forEach { commands.add(PathCommand.VerticalLineTo(it, false)) }
            "v" -> numbers.forEach { commands.add(PathCommand.VerticalLineTo(it, true)) }
            "C" -> {
                var i = 0
                while (i < numbers.size - 5) {
                    commands.add(PathCommand.CurveTo(numbers[i], numbers[i+1], numbers[i+2], numbers[i+3], numbers[i+4], numbers[i+5], false))
                    i += 6
                }
            }
            "c" -> {
                var i = 0
                while (i < numbers.size - 5) {
                    commands.add(PathCommand.CurveTo(numbers[i], numbers[i+1], numbers[i+2], numbers[i+3], numbers[i+4], numbers[i+5], true))
                    i += 6
                }
            }
            "S" -> {
                var i = 0
                while (i < numbers.size - 3) {
                    commands.add(PathCommand.SmoothCurveTo(numbers[i], numbers[i+1], numbers[i+2], numbers[i+3], false))
                    i += 4
                }
            }
            "s" -> {
                var i = 0
                while (i < numbers.size - 3) {
                    commands.add(PathCommand.SmoothCurveTo(numbers[i], numbers[i+1], numbers[i+2], numbers[i+3], true))
                    i += 4
                }
            }
            "Q" -> {
                var i = 0
                while (i < numbers.size - 3) {
                    commands.add(PathCommand.QuadTo(numbers[i], numbers[i+1], numbers[i+2], numbers[i+3], false))
                    i += 4
                }
            }
            "q" -> {
                var i = 0
                while (i < numbers.size - 3) {
                    commands.add(PathCommand.QuadTo(numbers[i], numbers[i+1], numbers[i+2], numbers[i+3], true))
                    i += 4
                }
            }
            "T" -> {
                var i = 0
                while (i < numbers.size - 1) {
                    commands.add(PathCommand.SmoothQuadTo(numbers[i], numbers[i+1], false))
                    i += 2
                }
            }
            "t" -> {
                var i = 0
                while (i < numbers.size - 1) {
                    commands.add(PathCommand.SmoothQuadTo(numbers[i], numbers[i+1], true))
                    i += 2
                }
            }
            "A" -> {
                var i = 0
                while (i < numbers.size - 6) {
                    commands.add(PathCommand.ArcTo(
                        numbers[i], numbers[i+1], numbers[i+2],
                        numbers[i+3] != 0f, numbers[i+4] != 0f,
                        numbers[i+5], numbers[i+6], false
                    ))
                    i += 7
                }
            }
            "a" -> {
                var i = 0
                while (i < numbers.size - 6) {
                    commands.add(PathCommand.ArcTo(
                        numbers[i], numbers[i+1], numbers[i+2],
                        numbers[i+3] != 0f, numbers[i+4] != 0f,
                        numbers[i+5], numbers[i+6], true
                    ))
                    i += 7
                }
            }
            "Z", "z" -> commands.add(PathCommand.ClosePath)
        }
    }
    return commands
}

fun parseNumbers(str: String): List<Float> {
    if (str.isBlank()) return emptyList()
    val numbers = mutableListOf<Float>()
    val regex = Regex("-?\\d*\\.?\\d+(?:[eE][+-]?\\d+)?")
    for (match in regex.findAll(str)) {
        try {
            numbers.add(match.value.toFloat())
        } catch (_: Exception) {
            // Skip invalid numbers
        }
    }
    return numbers
}

fun buildPath(commands: List<PathCommand>): Path {
    val path = Path()
    var currentX = 0f
    var currentY = 0f
    var startX = 0f
    var startY = 0f
    var lastControlX = 0f
    var lastControlY = 0f
    var lastCommand: PathCommand? = null

    for (cmd in commands) {
        when (cmd) {
            is PathCommand.MoveTo -> {
                if (cmd.relative) {
                    currentX += cmd.x
                    currentY += cmd.y
                } else {
                    currentX = cmd.x
                    currentY = cmd.y
                }
                path.moveTo(currentX, currentY)
                startX = currentX
                startY = currentY
            }
            is PathCommand.LineTo -> {
                if (cmd.relative) {
                    currentX += cmd.x
                    currentY += cmd.y
                } else {
                    currentX = cmd.x
                    currentY = cmd.y
                }
                path.lineTo(currentX, currentY)
            }
            is PathCommand.HorizontalLineTo -> {
                currentX = if (cmd.relative) currentX + cmd.x else cmd.x
                path.lineTo(currentX, currentY)
            }
            is PathCommand.VerticalLineTo -> {
                currentY = if (cmd.relative) currentY + cmd.y else cmd.y
                path.lineTo(currentX, currentY)
            }
            is PathCommand.CurveTo -> {
                val x1 = if (cmd.relative) currentX + cmd.x1 else cmd.x1
                val y1 = if (cmd.relative) currentY + cmd.y1 else cmd.y1
                val x2 = if (cmd.relative) currentX + cmd.x2 else cmd.x2
                val y2 = if (cmd.relative) currentY + cmd.y2 else cmd.y2
                val x = if (cmd.relative) currentX + cmd.x else cmd.x
                val y = if (cmd.relative) currentY + cmd.y else cmd.y
                path.cubicTo(x1, y1, x2, y2, x, y)
                lastControlX = x2
                lastControlY = y2
                currentX = x
                currentY = y
            }
            is PathCommand.SmoothCurveTo -> {
                val x1 = if (lastCommand is PathCommand.CurveTo || lastCommand is PathCommand.SmoothCurveTo) {
                    2 * currentX - lastControlX
                } else currentX
                val y1 = if (lastCommand is PathCommand.CurveTo || lastCommand is PathCommand.SmoothCurveTo) {
                    2 * currentY - lastControlY
                } else currentY
                val x2 = if (cmd.relative) currentX + cmd.x2 else cmd.x2
                val y2 = if (cmd.relative) currentY + cmd.y2 else cmd.y2
                val x = if (cmd.relative) currentX + cmd.x else cmd.x
                val y = if (cmd.relative) currentY + cmd.y else cmd.y
                path.cubicTo(x1, y1, x2, y2, x, y)
                lastControlX = x2
                lastControlY = y2
                currentX = x
                currentY = y
            }
            is PathCommand.QuadTo -> {
                val x1 = if (cmd.relative) currentX + cmd.x1 else cmd.x1
                val y1 = if (cmd.relative) currentY + cmd.y1 else cmd.y1
                val x = if (cmd.relative) currentX + cmd.x else cmd.x
                val y = if (cmd.relative) currentY + cmd.y else cmd.y
                path.quadraticTo(x1, y1, x, y)
                lastControlX = x1
                lastControlY = y1
                currentX = x
                currentY = y
            }
            is PathCommand.SmoothQuadTo -> {
                val x1 = if (lastCommand is PathCommand.QuadTo || lastCommand is PathCommand.SmoothQuadTo) {
                    2 * currentX - lastControlX
                } else currentX
                val y1 = if (lastCommand is PathCommand.QuadTo || lastCommand is PathCommand.SmoothQuadTo) {
                    2 * currentY - lastControlY
                } else currentY
                val x = if (cmd.relative) currentX + cmd.x else cmd.x
                val y = if (cmd.relative) currentY + cmd.y else cmd.y
                path.quadraticTo(x1, y1, x, y)
                lastControlX = x1
                lastControlY = y1
                currentX = x
                currentY = y
            }
            is PathCommand.ArcTo -> {
                val arcX = if (cmd.relative) currentX + cmd.x else cmd.x
                val arcY = if (cmd.relative) currentY + cmd.y else cmd.y
                drawArc(path, currentX, currentY, arcX, arcY, cmd.rx, cmd.ry, cmd.xAxisRotation, cmd.largeArcFlag, cmd.sweepFlag)
                currentX = arcX
                currentY = arcY
            }
            is PathCommand.ClosePath -> {
                path.close()
                currentX = startX
                currentY = startY
            }
        }
        lastCommand = cmd
    }
    return path
}

// Arc drawing helper
fun drawArc(
    path: Path,
    x1: Float, y1: Float,
    x2: Float, y2: Float,
    rx: Float, ry: Float,
    xAxisRotation: Float,
    largeArcFlag: Boolean,
    sweepFlag: Boolean
) {
    if (rx == 0f || ry == 0f) {
        path.lineTo(x2, y2)
        return
    }

    val phi = Math.toRadians(xAxisRotation.toDouble())
    val cosPhi = cos(phi)
    val sinPhi = sin(phi)

    val dx = (x1 - x2) / 2.0
    val dy = (y1 - y2) / 2.0

    val x1p = cosPhi * dx + sinPhi * dy
    val y1p = -sinPhi * dx + cosPhi * dy

    var rxAbs = abs(rx).toDouble()
    var ryAbs = abs(ry).toDouble()

    val lambda = (x1p * x1p) / (rxAbs * rxAbs) + (y1p * y1p) / (ryAbs * ryAbs)
    if (lambda > 1) {
        val sqrtLambda = sqrt(lambda)
        rxAbs *= sqrtLambda
        ryAbs *= sqrtLambda
    }

    val rxSq = rxAbs * rxAbs
    val rySq = ryAbs * ryAbs
    val x1pSq = x1p * x1p
    val y1pSq = y1p * y1p

    var sq = ((rxSq * rySq) - (rxSq * y1pSq) - (rySq * x1pSq)) / ((rxSq * y1pSq) + (rySq * x1pSq))
    if (sq < 0) sq = 0.0
    val coef = (if (largeArcFlag == sweepFlag) -1 else 1) * sqrt(sq)

    val cxp = coef * rxAbs * y1p / ryAbs
    val cyp = -coef * ryAbs * x1p / rxAbs

    val cx = cosPhi * cxp - sinPhi * cyp + (x1 + x2) / 2.0
    val cy = sinPhi * cxp + cosPhi * cyp + (y1 + y2) / 2.0

    val theta1 = angle(1.0, 0.0, (x1p - cxp) / rxAbs, (y1p - cyp) / ryAbs)
    var dtheta = angle((x1p - cxp) / rxAbs, (y1p - cyp) / ryAbs, (-x1p - cxp) / rxAbs, (-y1p - cyp) / ryAbs)

    if (!sweepFlag && dtheta > 0) dtheta -= 2 * PI
    else if (sweepFlag && dtheta < 0) dtheta += 2 * PI

    val segments = ceil(abs(dtheta) / (PI / 2)).toInt()
    val delta = dtheta / segments
    val t = 8.0 / 3.0 * sin(delta / 4) * sin(delta / 4) / sin(delta / 2)

    var cosTheta1 = cos(theta1)
    var sinTheta1 = sin(theta1)

    for (i in 0 until segments) {
        val theta2 = theta1 + (i + 1) * delta
        val cosTheta2 = cos(theta2)
        val sinTheta2 = sin(theta2)

        val ep1x = cx + rxAbs * (cosPhi * cosTheta1 - sinPhi * sinTheta1)
        val ep1y = cy + ryAbs * (sinPhi * cosTheta1 + cosPhi * sinTheta1)
        val ep2x = cx + rxAbs * (cosPhi * cosTheta2 - sinPhi * sinTheta2)
        val ep2y = cy + ryAbs * (sinPhi * cosTheta2 + cosPhi * sinTheta2)

        val dx1 = -rxAbs * (cosPhi * sinTheta1 + sinPhi * cosTheta1)
        val dy1 = -ryAbs * (sinPhi * sinTheta1 - cosPhi * cosTheta1)
        val dx2 = -rxAbs * (cosPhi * sinTheta2 + sinPhi * cosTheta2)
        val dy2 = -ryAbs * (sinPhi * sinTheta2 - cosPhi * cosTheta2)

        path.cubicTo(
            (ep1x + t * dx1).toFloat(),
            (ep1y + t * dy1).toFloat(),
            (ep2x - t * dx2).toFloat(),
            (ep2y - t * dy2).toFloat(),
            ep2x.toFloat(),
            ep2y.toFloat()
        )

        cosTheta1 = cosTheta2
        sinTheta1 = sinTheta2
    }
}

fun angle(ux: Double, uy: Double, vx: Double, vy: Double): Double {
    val n = sqrt(ux * ux + uy * uy) * sqrt(vx * vx + vy * vy)
    if (n == 0.0) return 0.0
    var c = (ux * vx + uy * vy) / n
    c = c.coerceIn(-1.0, 1.0)
    val angle = acos(c)
    return if (ux * vy - uy * vx < 0) -angle else angle
}

// Create path from string
fun createPathFromString(pathData: String): Path {
    return buildPath(parsePathData(pathData))
}

