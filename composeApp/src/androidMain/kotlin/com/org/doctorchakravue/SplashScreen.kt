// Animation state holder
data class CharacterAnimState(
    val character: LogoCharacter,
    val path: Path,
    // 3D scatter positions
    var scatterX: Float = 0f,
    var scatterY: Float = 0f,
    var scatterZ: Float = 0f, // Z depth (0 = far, 1 = close)
    // Target positions (center)
    var targetX: Float = 0f,
    var targetY: Float = 0f,
    // Animation values
    var alpha: Float = 0f,
    var scale: Float = 1f,
    var rotationAngle: Float = 0f,
    // Reveal progress (for non-main chars)
    var revealProgress: Float = 0f,
    // Circular position angle
    var circularAngle: Float = 0f
)

@Composable
fun SplashScreen(
    onSplashComplete: () -> Unit = {}
) {
    val density = LocalDensity.current
    var screenWidth by remember { mutableStateOf(0f) }
    var screenHeight by remember { mutableStateOf(0f) }
    val logoCenterX by remember { derivedStateOf { screenWidth / 2f } }
    val logoCenterY by remember { derivedStateOf { screenHeight / 2f } }
    val viewportWidth = 210f
    val viewportHeight = 297f
    val logoScaleX = 1.6666666f
    val logoScaleY = 2.357143f
    val logoTranslateX = -74.583336f
    val logoTranslateY = -133.47322f
    val characters = remember { getAllLogoCharacters() }
    val characterStates = remember {
        mutableStateListOf<CharacterAnimState>().apply {
            characters.forEach { char ->
                add(CharacterAnimState(
                    character = char,
                    path = createPathFromString(char.pathData)
                ))
            }
        }
    }
    // Animation state: position, velocity, and scale/alpha
    data class CharAnimState(
        var posX: Float = 0f,
        var posY: Float = 0f,
        var velX: Float = 0f,  // velocity in X direction
        var velY: Float = 0f,  // velocity in Y direction
        var progress: Float = 0f  // 0 to 1, animation progress
    )

    val animStates = remember { characterStates.map { CharAnimState() } }
    val animProgress = remember { Animatable(0f) }

    // Phase 3 animation state - rotate back to original and scale to final logo size
    val phase3Progress = remember { Animatable(0f) }

    // Derived state for phase3 progress value
    val phase3Prog by remember { derivedStateOf { phase3Progress.value } }

    // Angular positions for all characters arranged in circular path
    val allCharacterAngles = remember {
        floatArrayOf(
            // All characters distributed around circle
            270f, 90f, 180f, 0f,
            // Remaining characters distributed around circle
            45f, 135f, 225f, 315f,
            50f, 140f, 230f, 320f,
            55f, 145f, 235f, 325f,
            60f, 150f, 240f, 330f,
            65f, 155f, 245f, 335f,
            70f, 160f, 250f, 340f,
            75f, 165f, 255f, 345f,
            80f, 170f, 260f, 350f,
            85f, 175f, 265f, 355f,
            10f, 20f, 30f, 40f
        )
    }

    // Initialize Phase 1: positions and velocities - ALL within screen boundaries
    LaunchedEffect(screenWidth, screenHeight) {
        if (screenWidth > 0 && screenHeight > 0) {
            val random = java.util.Random(42)

            // Safe boundaries (20% margin from edges)
            val margin = minOf(screenWidth, screenHeight) * 0.2f
            val minX = margin
            val maxX = screenWidth - margin
            val minY = margin
            val maxY = screenHeight - margin
            val safeWidth = maxX - minX
            val safeHeight = maxY - minY

            animStates.forEachIndexed { idx, state ->
                // Spawn at random position WITHIN safe boundaries
                state.posX = minX + random.nextFloat() * safeWidth
                state.posY = minY + random.nextFloat() * safeHeight

                // Random movement direction with limited speed to stay within bounds
                val moveAngle = random.nextFloat() * 360f
                val moveSpeed = random.nextFloat() * 80f + 40f  // 40-120 pixels (reduced speed)

                state.velX = moveSpeed * cos(Math.toRadians(moveAngle.toDouble())).toFloat()
                state.velY = moveSpeed * sin(Math.toRadians(moveAngle.toDouble())).toFloat()
            }
        }
    }

    // PHASE 1: Scatter animation with fade - characters spawn from all directions + center
    LaunchedEffect(screenWidth, screenHeight) {
        if (screenWidth <= 0 || screenHeight <= 0) return@LaunchedEffect

        val phase1Duration = 1200 // Reduced from 1800ms for faster start
        val phase3Duration = 2800 // Slightly extended for slower spin

        // Animate progress from 0 to 1 for full Phase 1 duration
        animProgress.animateTo(
            targetValue = 1f,
            animationSpec = tween(durationMillis = phase1Duration, easing = LinearEasing)
        )

        // Phase 3 starts immediately after Phase 1 completes (no delay)
        phase3Progress.animateTo(
            targetValue = 1f,
            animationSpec = tween(durationMillis = phase3Duration, easing = LinearEasing)
        )

        // Animation complete - call onSplashComplete immediately (no extra delay)
        onSplashComplete()
    }
    Box(
        modifier = Modifier
            .fillMaxSize()
            .background(Color.Black)
    ) {
        Canvas(
            modifier = Modifier
                .fillMaxSize()
                .onGloballyPositioned { coordinates ->
                    screenWidth = coordinates.size.width.toFloat()
                    screenHeight = coordinates.size.height.toFloat()
                }
        ) {
            if (screenWidth <= 0 || screenHeight <= 0) return@Canvas
            val baseLogoScale = minOf(
                size.width / (viewportWidth * logoScaleX) * 0.8f,
                size.height / (viewportHeight * logoScaleY) * 0.8f
            )
            val centerX = size.width / 2f
            val centerY = size.height / 2f
            val progress = animProgress.value // Phase 1 progress

            // PHASE 1: Render all characters moving and fading - ALL WITHIN SCREEN BOUNDS
            if (progress < 1f) {
                // Safe boundaries (10% margin from edges for larger characters)
                val margin = minOf(size.width, size.height) * 0.10f
                val minX = margin
                val maxX = size.width - margin
                val minY = margin
                val maxY = size.height - margin

                // Logo center in original SVG coordinates (paths are around 70-150)
                val logoOriginX = 108f
                val logoOriginY = 119f

                // Scale factor - TRIPLED from 15% to 45% of screen for high visibility
                val baseScale = minOf(size.width, size.height) * 0.45f / 80f

                // Fade start progress: characters start fading only in the last 500ms
                // With phase1Duration = 1800ms, fadeStartProgress = 1300/1800 = 0.722f
                val fadeStartProgress = 1300 / 1800f

                characterStates.forEachIndexed { i, state ->
                    val animState = animStates[i]

                    // Update position based on velocity and progress
                    // INCREASED distance multiplier from 1.5f to 3.0f for greater travel
                    val rawX = animState.posX + (animState.velX * progress * 3.0f)
                    val rawY = animState.posY + (animState.velY * progress * 3.0f)
                    val currentX = rawX.coerceIn(minX, maxX)
                    val currentY = rawY.coerceIn(minY, maxY)

                    // Scale: starts at base, grows a bit, then shrinks as it fades
                    val scaleMultiplier = baseScale * (0.8f + progress * 0.8f * (1f - progress))

                    // Alpha: Phase 1 fade + Phase 3 fade-in effect
                    // Characters fade out as Phase 3 starts to fade in
                    val phase1Alpha = when {
                        progress < fadeStartProgress -> 1f // Fully visible until fade starts
                        else -> {
                            // Fade out over the last 500ms
                            val fadeProgress = (progress - fadeStartProgress) / (1f - fadeStartProgress)
                            (1f - fadeProgress).coerceIn(0f, 1f)
                        }
                    }

                    // As Phase 3 starts (phase3Prog > 0), Phase 1 characters fade out more
                    val phase3FadeOut = (1f - phase3Prog).coerceIn(0f, 1f)
                    val alpha = phase1Alpha * phase3FadeOut

                    withTransform({
                        // 1. Move to screen position
                        translate(currentX, currentY)
                        // 2. Apply scale
                        scale(scaleMultiplier, scaleMultiplier, Offset.Zero)
                        // 3. Center the logo path at origin (subtract logo center)
                        translate(-logoOriginX, -logoOriginY)
                    }) {
                        drawPath(
                            path = state.path,
                            color = Color.White.copy(alpha = alpha),
                            style = Fill
                        )
                    }
                }
            }


            // PHASE 3: All characters assemble with smooth continuous animation
            if (phase3Prog > 0f) {
                val logoOriginX = 108f
                val logoOriginY = 119f

                // Final logo scale at screen center - match Phase 1 baseScale size
                val finalLogoScale = minOf(size.width, size.height) * 0.45f / 80f

                // Smooth ease-out curve applied to entire animation (no hard phase boundaries)
                // Using smooth ease-out: starts fast, gradually slows down
                val smoothProgress = 1f - ((1f - phase3Prog) * (1f - phase3Prog)) // Quadratic ease-out

                // For extra smooth settling at the end, blend with cubic ease-out in last portion
                val settleBlend = (phase3Prog - 0.7f).coerceIn(0f, 0.3f) / 0.3f // 0 to 1 in last 30%
                val cubicEase = 1f - ((1f - phase3Prog) * (1f - phase3Prog) * (1f - phase3Prog))
                val blendedProgress = smoothProgress * (1f - settleBlend) + cubicEase * settleBlend

                // Rotation: 1.5 spins (540 degrees) with smooth deceleration throughout
                // Slower spin for more elegant animation
                val totalSpinDegrees = 540f
                val rotation = totalSpinDegrees * smoothProgress

                // Scale: smoothly shrink from 3x to 1x throughout the animation
                val initialScale = 3f
                val currentScale = finalLogoScale * (initialScale - (blendedProgress * (initialScale - 1f)))

                // Radius: smoothly shrink to center throughout animation
                val charRadius = minOf(size.width, size.height) * 0.12f
                val currentRadius = charRadius * (1f - blendedProgress)

                characterStates.forEachIndexed { idx, state ->
                    if (idx < allCharacterAngles.size) {
                        val charAngle = allCharacterAngles[idx]

                        val angleRad = Math.toRadians(charAngle.toDouble())
                        val offsetX = currentRadius * cos(angleRad).toFloat()
                        val offsetY = currentRadius * sin(angleRad).toFloat()

                        val posX = centerX + offsetX
                        val posY = centerY + offsetY

                        withTransform({
                            translate(posX, posY)
                            rotate(rotation, Offset.Zero)
                            scale(currentScale, currentScale, Offset.Zero)
                            translate(-logoOriginX, -logoOriginY)
                        }) {
                            drawPath(
                                path = state.path,
                                color = Color.White,
                                style = Fill
                            )
                        }
                    }
                }
            }
        }
    }
}
